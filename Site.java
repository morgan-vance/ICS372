
public class Site {
	private String locationID;
	private String siteID;
	private String rdgType;
	private int rdgValue;
	private String rdgDate;
	
	public String getLocationID() {
		return locationID;
	}
	public void setLocationID(String locationID) {
		this.locationID = locationID;
	}
	public String getSiteID() {
		return siteID;
	}
	public void setSiteID(String siteID) {
		this.siteID = siteID;
	}
	public String getRdgType() {
		return rdgType;
	}
	public void setRdgType(String rdgType) {
		this.rdgType = rdgType;
	}
	public int getRdgValue() {
		return rdgValue;
	}
	public void setRdgValue(int rdgValue) {
		this.rdgValue = rdgValue;
	}
	public String getRdgDate() {
		return rdgDate;
	}
	public void setRdgDate(String rdgDate) {
		this.rdgDate = rdgDate;
	}
	
	
}
